export function StraightLines() : Array<{ x0: number, y0: number, x1: number, y1: number }> {
    return Inputs.filter((line) => {
        let x1 = Math.max(line.x1, line.x0),
            x0 = Math.min(line.x1, line.x0);
        let y1 = Math.max(line.y1, line.y0),
            y0 = Math.min(line.y1, line.y0);
        
        let length = x1 - x0;
        let height = y1 - y0;

        return x1 === x0 || y1 === y0 || length === height;
    });
}

export function Max(lines: Array<{ x0: number, y0: number, x1: number, y1: number }>) : { x: number, y: number } {
    let max = { x: 0, y: 0 };
    lines.forEach(line => {
        if (line.x0 > max.x) { max.x = line.x0; }
        if (line.x1 > max.x) { max.x = line.x1; }
        if (line.y0 > max.y) { max.y = line.y0; }
        if (line.y1 > max.y) { max.y = line.y1; }
    })
    return max;
}